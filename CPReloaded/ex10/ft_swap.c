#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

int	main(void)
{
	int	x;
	int	y;

	x = 12;
	y = 34;
	printf (" x = %d ; y = %d\n", x, y);
	ft_swap(&x, &y);
	printf (" x = %d ; y = %d\n", x, y);
}
